//WRITE COMMAND

void Write_Cmd(unsigned char CMD)
{ unsigned char i;
 	 
    LCD_CS=0;
   	LCD_RS=0;
    for(i=0;i<8;i++)
     {  LCD_SCL=0;
        if(CMD&0x80) LCD_SDA=1;
       	else LCD_SDA=0;
       	LCD_SCL=1;
        CMD=CMD<<1;
     }
   	LCD_CS=1;


}


void disp_ch(uint8 form, uint16 x, uint16 y,uint8 value,uint16 dcolor,uint16 bgcolor)	
{  
	uint16 i,j;
	uint8 const *temp ;//= chlibsmall;  
  uint16 cp = 0;
	uint16 pp = 0;
	uint16 cnum;
	

	temp = chlibsmall;
	cp = 16; 
	pp = 30;
	temp += (value - 32) * 60;		

	LCD_SetPos(x,x+cp-1,y,y+pp-1);
	cnum = pp*cp/8;  
  	
	for(j = 0;j < cnum;j++)
		{
		for(i=0;i<8;i++)
			{ 		     
		 	if(((*temp >> i) & 0x01)!=0)
				{
				Write_Data(dcolor>>8,dcolor);
				} 
			else
				{
				Write_Data(bgcolor>>8,bgcolor);
				} 	
			}
		temp++;
		}
}


void Lcd_Show_String(uint16 x, uint16 y,char *str,uint8 mode,uint8 format,uint16 dcolor,uint16 bgcolor)	  
{  
	unsigned int x1,y1;
  unsigned int i,len;	
	x1 = y * 15;
	y1 = x * 30;
	len = strlen(str);
	while(*str!='\0')
	{			
		disp_ch(0,x1,y1,*str,dcolor,bgcolor);
		x1+=15;	
		str++;
	}	
	if(format > 21) format = 21;
	if(strlen(str) < format)
	{
		x1 = len * 15;
		for(i = len; i < format;i++)
		{	
			disp_ch(0,x1,y1,' ',dcolor,bgcolor);
			x1+=15;	
		}
	}
}


